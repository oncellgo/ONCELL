if (!i18n.isInitialized) {
  i18n.use(initReactI18next).init({
    resources,
    lng: 'ko',
    fallbackLng: 'ko',
    ns: ['common'],
    defaultNS: 'common',
    interpolation: { escapeValue: false },
    react: { useSuspense: false },
    initImmediate: false,
  });
} else {
  // HMR: module is re-executed but i18n singleton persists.
  // Force-refresh resource bundles so new translations take effect without a server restart.
  (Object.keys(resources) as Array<keyof typeof resources>).forEach((lng) => {
    const bundles = resources[lng] as Record<string, any>;
    Object.keys(bundles).forEach((ns) => {
      i18n.addResourceBundle(lng, ns, bundles[ns], true, true);
    });
  });
}

export const detectAndApplyClientLang = () => {
  if (typeof window === 'undefined') return;
  const stored = window.localStorage.getItem('lang');
  let lng: 'ko' | 'en' | 'zh' = 'ko';
  if (stored === 'ko' || stored === 'en' || stored === 'zh') {
    lng = stored;
  } else {
    const nav = window.navigator.language.toLowerCase();
    if (nav.startsWith('zh')) lng = 'zh';
    else if (nav.startsWith('en')) lng = 'en';
  }
  if (i18n.language !== lng) i18n.changeLanguage(lng);
};

export const changeLanguage = (lng: 'ko' | 'en' | 'zh') => {
  if (typeof window !== 'undefined') window.localStorage.setItem('lang', lng);
  return i18n.changeLanguage(lng);
};

export default i18n;
